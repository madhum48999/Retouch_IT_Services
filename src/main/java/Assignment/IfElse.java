package Assignment;

public class IfElse {
    public static void main(String[] args) {
        //initializing variable score
        int score = 40;

        //condition if score greater than equal to 50 for passed
        if(score >= 50){
            System.out.println("Passed");
        }

        //else less than 50 is Failed
        else{

            System.out.println("Failed");
        }
    }
}
